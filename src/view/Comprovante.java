    public static void mostrarRegistro(Registro registro){
        comprovante.setVisible(true);
        
        comprovante.nome.setText(registro.getFuncionario().getNome());
        comprovante.idPessoa.setText(String.valueOf(registro.getFuncionario().getId()));
        comprovante.pis.setText(registro.getFuncionario().getPis());
        comprovante.hora.setText(String.valueOf(registro.getData()));
        comprovante.tipo.setText(String.valueOf(registro.getTipo().name()));
        try {
            Thread.sleep(4500);
        } catch (InterruptedException ex) {
            Logger.getLogger(Comprovante.class.getName()).log(Level.SEVERE, null, ex);
        }
        comprovante.setVisible(false);
    }

    
    
